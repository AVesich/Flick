import SwiftUI

extension Font {
    static func sentient(size: CGFloat) -> Font {
        .custom("Sentient-Medium", size: size)
    }

    static func olympe(size: CGFloat) -> Font {
        .custom("Olympe Mono Hacked", size: size)
    }
}
